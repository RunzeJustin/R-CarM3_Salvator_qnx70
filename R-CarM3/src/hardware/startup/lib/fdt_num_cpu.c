#include "startup.h"
#include <libfdt.h>

unsigned
fdt_num_cpu(void) {
	unsigned count = 0;

	if(fdt != NULL) {
		int node = -1;
		for( ;; ) {
			node = fdt_node_offset_by_prop_value(fdt, node, "device_type", "cpu", sizeof "cpu");
			if(node < 0) break;
			++count;
		}
	}
	return count;
}

