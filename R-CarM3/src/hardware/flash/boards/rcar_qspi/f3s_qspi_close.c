#include "f3s_spi.h"

/*
 * This is the close callout for SPI serial NOR flash.
 */
void f3s_qspi_close(f3s_socket_t *socket, uint32_t flags)
{
    spi_flash_t *spic = (spi_flash_t *)socket->socket_handle;

    spic->dinit(socket->socket_handle);
}

